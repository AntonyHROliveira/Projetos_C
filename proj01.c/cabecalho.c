/*1. #include <stdio.h>

    O que é:
    => stdio.h é um cabeçalho padrão da biblioteca C que contém definições para funções de entrada e saída (I/O).
    
    Para que serve:
    => Ele permite que você use funções como printf() para imprimir dados na tela e scanf() para ler dados do usuário. É essencial para qualquer programa que precise interagir com o usuário ou com arquivos.
*/

/*2. #include <locale.h>

    O que é: 
    => locale.h é um cabeçalho que fornece funções e macros para manipulação de localidade (locale) em C.
    
    Para que serve:
    => Ele permite que você defina a localidade do seu programa, o que é importante para a formatação de números, datas e para o suporte a caracteres especiais e acentos. Isso é especialmente útil em programas que precisam lidar com diferentes idiomas e formatos regionais.
*/

/*3. #include <stdlib.h>

    O que é:
    => stdlib.h é um cabeçalho que contém funções para manipulação de memória, controle de processos, conversões de tipos e outras utilidades gerais.
    
    Para que serve:
    => Ele fornece funções como malloc() e free() para alocação e liberação de memória dinâmica, exit() para terminar um programa, e atoi(), atof() para conversão de strings em números inteiros e de ponto flutuante, respectivamente.
*/

/* Resumo

    <stdio.h>: Para entrada e saída padrão.
    <locale.h>: Para manipulação de localidade e suporte a caracteres especiais.
    <stdlib.h>: Para funções utilitárias, como alocação de memória e conversões.
*/